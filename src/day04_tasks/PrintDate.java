package day04_tasks;

public class PrintDate {

    public static void main(String[] args) {
        int month = 2;
        int day = 16;
        int year = 2023;
        System.out.println(month + "/" + day + "/" + year );
        System.out.println(month + "-" + day + "-" + year);
        System.out.println(month + "|" + day + "|" + year);
        System.out.println(month + "<>"+ day + "<>" +year);




    }
}
